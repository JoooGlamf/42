char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (1)
	{
		dest[i] = src[i];
		if (src[i] == '\0')
		{
			return (dest);
		}
		i++;
	}
	return (dest);
}
